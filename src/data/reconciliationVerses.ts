export function getReconciliationVerses(): BibleVerse[] {
  return reconciliationVerses;
}

export function getVersesByVersion(version?: string): BibleVerse[] {
  if (!version) return reconciliationVerses;
  return reconciliationVerses.filter(verse => verse.version === version);
}

export function getAvailableVersions(): string[] {
  const versions = [...new Set(reconciliationVerses.map(verse => verse.version))];
  return versions.sort();
}

export function getVersionStats(): { version: string; count: number }[] {
  const versionMap = new Map<string, number>();

  reconciliationVerses.forEach(verse => {
    versionMap.set(verse.version, (versionMap.get(verse.version) || 0) + 1);
  });

  return Array.from(versionMap.entries())
    .map(([version, count]) => ({ version, count }))
    .sort((a, b) => b.count - a.count);
}

export function highlightReconciliationText(text: string): string {
  const wordsToHighlight = [
    // Core reconciliation terms
    'reconciliation', 'reconcile', 'reconciled', 'reconciling', 'atonement',
    // Forgiveness terms
    'forgive', 'forgiveness', 'forgiven', 'forgives', 'forgiving', 'pardon', 'pardons', 'pardoned',
    // Restoration terms
    'restore', 'restoration', 'restored', 'restores', 'restoring', 'renew', 'renewal', 'renewed',
    // Peace terms
    'peace', 'peaceable', 'peacemakers', 'peaceful', 'peaceably',
    // Unity terms
    'unity', 'united', 'together', 'one', 'harmony', 'harmonious',
    // Healing terms
    'heal', 'healing', 'healed', 'heals', 'whole', 'wholeness',
    // Mercy & grace terms
    'mercy', 'merciful', 'mercies', 'grace', 'gracious', 'compassion', 'compassionate',
    // Love terms
    'love', 'loves', 'loved', 'loving', 'beloved', 'steadfast love',
    // Repentance terms
    'repent', 'repentance', 'repents', 'repented', 'return', 'returns', 'returned',
    // Salvation terms
    'save', 'saves', 'saved', 'salvation', 'redeem', 'redeemed', 'redemption'
  ];

  let highlightedText = text;

  wordsToHighlight.forEach(word => {
    const regex = new RegExp(`(${word})`, 'gi');
    highlightedText = highlightedText.replace(regex, '<mark class="bg-yellow-200 px-1 rounded">$1</mark>');
  });

  return highlightedText;
}
